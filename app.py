def ask_ai_consultant(user_id, user_text):
    history = dialog_history.get(user_id, [])
    history.append({"role": "user", "content": user_text})
    history = history[-MAX_HISTORY_MESSAGES:]

    response = client.messages.create(
        model="claude-sonnet-5",
        max_tokens=500,
        system=SYSTEM_PROMPT.format(knowledge_base=KNOWLEDGE_BASE),
        messages=history,
    )

    answer = response.content[0].text
    history.append({"role": "assistant", "content": answer})
    dialog_history[user_id] = history[-MAX_HISTORY_MESSAGES:]

    return answer


def send_message(user_id, text):
    requests.post(
        "https://api.vk.com/method/messages.send",
        params={
            "access_token": VK_GROUP_TOKEN,
            "v": API_VERSION,
            "user_id": user_id,
            "message": text,
            "random_id": 0,
        },
        timeout=10,
    )


@app.route("/callback", methods=["POST"])
def callback():
    data = request.json

    if data.get("secret") != VK_SECRET_KEY:
        return "invalid secret", 403

    event_type = data.get("type")

    if event_type == "confirmation":
        return VK_CONFIRMATION_CODE

    if event_type == "message_new":
        message = data["object"]["message"]
        user_id = message["from_id"]
        text = message.get("text", "")

        if text.strip():
            try:
                answer = ask_ai_consultant(user_id, text)
            except Exception as e:
                print("Ошибка при обращении к ИИ:", e)
                answer = (
                    "Извините, сейчас не получается ответить. "
                    "Попробуйте написать чуть позже, либо дождитесь ответа администратора."
                )
            send_message(user_id, answer)

        return "ok"

    return "ok"


